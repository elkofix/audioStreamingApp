package models;
/**
 * Categories podcasts can fall in
 * {@link #POLITICS}
 * {@link #ENTERTAINMENT}
 * {@link #VIDEOGAMES}
 * {@link #TREND}
 */
public enum Category {
	/**
	 * Politics category
	 */
	POLITICS,
	/**
	 * Entertainment category
	 */
	ENTERTAINMENT,
	/**
	 * Videogames category
	 */
	VIDEOGAMES,
	/**
	 * Trending category
	 */
	TREND
}